package Lesson5;

public class Main {
    public static void main(String[] args) {

        int a = 56;
        int b = 66;

        if (a > b) {
            System.out.println(a + " > b");
        }   else {
            System.out.println(a + " < b");
        }


        if (a % 2 == 0) {
            System.out.println(a + " is even");
        } else {
            System.out.println(a + " is odd");
        }


    }
}
